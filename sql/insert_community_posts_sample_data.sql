-- 삽입 결과 확인
SELECT 
    id,
    category,
    title,
    member_user_id,
    views,
    likes,
    answers_count,
    ishot,
    created_at
FROM community_posts 
WHERE member_user_id IN ('투자고수', '부자되기', '절약왕', '코인러버', '투자초보')
ORDER BY created_at DESC;

-- 통계 확인
SELECT 
    category,
    COUNT(*) as post_count,
    SUM(views) as total_views,
    SUM(likes) as total_likes,
    SUM(answers_count) as total_comments
FROM community_posts 
WHERE member_user_id IN ('투자고수', '부자되기', '절약왕', '코인러버', '투자초보')
GROUP BY category
ORDER BY post_count DESC; 